#include "libft.h"

char	*ft_strdup(char *s)
{
	char	*arr;
	int		i;

	arr = NULL;
	arr = (char *) malloc(sizeof(char) * (ft_strlen(s) + 1));
	i = 0;
	if (!arr)
		return (NULL);
	while (s[i])
	{
		arr[i] = s[i];
		i++;
	}
	arr[i] = '\0';
	return (arr);
}
